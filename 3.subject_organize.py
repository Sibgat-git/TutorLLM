# Compile all patterns
COMPILED_RULES = [
    (name, [re.compile(p, re.IGNORECASE) for p in patterns])
    for name, patterns in SUBJECT_RULES
]


def classify(subject: str) -> str:
    for category, patterns in COMPILED_RULES:
        if any(p.search(subject) for p in patterns):
            return category
    return "other"


with open(input_file, "r", encoding="utf-8") as f:
    data = json.load(f)

buckets: dict[str, list] = {}
for q in data:
    cat = classify(q.get("subject", ""))
    buckets.setdefault(cat, []).append(q)

for cat, questions in sorted(buckets.items()):
    path = os.path.join(output_dir, f"{cat}.json")
    with open(path, "w", encoding="utf-8") as f:
        json.dump(questions, f, ensure_ascii=False, indent=2)
    print(f"{cat:20s} : {len(questions):5d}  -> {path}")

print(f"\nTotal distributed: {sum(len(v) for v in buckets.values())} / {len(data)}")
